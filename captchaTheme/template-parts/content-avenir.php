        <br>
        <h4>Génie en techniques de l'information</h4>
        <p>Ce baccalauréat vous spécialisera en conception de systèmes d’information pour l'entreprise. Vous
            travaillerez à la conception, à l'adaptation et à la mise en place d’infrastructures matérielles et
            logicielles dans le but de répondre aux besoins des entreprises et d'améliorer leur rendement.</p>
        <br>
        <h4>Génie logiciel</h4>
        <p>Le programme de baccalauréat en génie logiciel vous apprendra entre autres à concevoir et à développer des
            applications commerciales, des systèmes de contrôle de réseau et des jeux vidéos. Il vous préparera à
            occuper des postes notamment d’ingénieur logiciel, de responsable du développement, d'analyste, de
            consultant ou de concepteur de logiciel.</p>
