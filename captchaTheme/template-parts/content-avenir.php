<p><a href="https://www.uqat.ca/etudes/creation-et-nouveaux-medias/baccalaureat-en-creation-numerique-profils-informatique-cinema-creation-3d-technologie-web/">DEC-BAC en création numérique – concentration Cinéma, Création 3D ou Technologie Web à l’UQAT (Rouyn-Noranda)</a></p>
<br>
<p>Le baccalauréat en création numérique vise à développer chez l’étudiant des
habiletés créatives et conceptuelles dans plusieurs disciplines convergentes dont la
création cinématographique et vidéographique, le design d’images de synthèse et
l’intégration Web, ainsi que la communication graphique. Les étudiants seront
amenés à tirer parti des possibilités liées aux technologies numériques de pointe
dans le cadre d’un projet professionnel, ainsi qu’à devenir des architectes d’univers
virtuels en exploitant les outils de création numérique. Le baccalauréat en création
numérique s’adresse à des individus créatifs intéressés par les diverses facettes
des médias numériques. <br>
Les profils offerts visent à parfaire la formation de l’étudiant en lui permettant de se
spécialiser dans le domaine de la création cinématographique, de la création 3D ou
de la technologie Web.</p>
</section>
