<p><a rel="noreferrer noopener" href="https://www.uqat.ca/etudes/creation-et-nouveaux-medias/baccalaureat-en-creation-de-jeux-video-profils-art-et-design/" target="_blank">DEC-BAC en création de jeux vidéo – Intégration logicielle à l’UQAT (Montréal)</a></p>
<br>
<p>Le baccalauréat en création de jeux vidéo est une formation sur mesure pour la
création et le développement de jeux vidéo. Il vise le développement de la
créativité, de l’autonomie et du travail d’équipe par la création et l’étude d’œuvres
vidéoludiques. <br>
L’étudiant pourra s’inscrire à l’un des deux profils offerts : art ou design. Ensuite, il
pourra choisir un champ de spécialisation spécifique à ce profil et le développer
dans des ateliers mis en place à cet effet. <br>
Le profil art permet à l’étudiant de comprendre et d’adopter une approche artistique,
créative et technique visant à la fois les compétences conceptuelles et appliquées
en création 3D, mais aussi de choisir une spécialisation en animation, en
squelettage (rigging), en édition de matériaux (shader), etc. <br>
Le profil design vise plutôt le développement d’une approche esthétique et
systémique en fonction des règles et de l’expérience de jeu, tout en offrant à
l’étudiant une spécialisation de son choix comme le design narratif, le design
pédagogique, le design d’interfaces pour utilisateurs, l’intelligence artificielle, etc. </p>
